const LOGICAL_FIELD_ALIASES = new Map([
  ['business_name', 'name'],
  ['status', 'lead_status'],
  ['notes', 'notes']
]);

function getBaseUrl(): string {
  validateEnv(ENV_REQUIREMENTS.airtable);
  const baseId = getEnv('airtableBaseId');
  const tableName = encodeURIComponent(getEnvOptional('airtableTableName', 'Leads'));
  return `https://api.airtable.com/v0/${baseId}/${tableName}`;
}

function getMetadataBaseUrl(): string {
  validateEnv(ENV_REQUIREMENTS.airtable);
  return `https://api.airtable.com/v0/meta/bases/${getEnv('airtableBaseId')}`;
}

function getHeaders(): Record<string, string> {
  return {
    Authorization: `Bearer ${getEnv('airtableApiKey')}`,
    'Content-Type': 'application/json'
  };
}

async function getConfiguredTable(): Promise<AirtableTable> {
  const response = await fetch(`${getMetadataBaseUrl()}/tables`, { headers: getHeaders() });
  if (!response.ok) {
    throw new Error(`Airtable schema lookup failed: ${response.status} ${response.statusText} — ${await response.text()}`);
  }

  const payload = (await response.json()) as AirtableSchemaResponse;
  const configured = getEnvOptional('airtableTableName', 'Leads');
  const table = payload.tables?.find(candidate => candidate.id === configured || candidate.name === configured);
  if (!table) {
    throw new Error(`Table Airtable introuvable: ${configured}`);
  }

  return table;
}

function buildCanonicalFieldMap(table: AirtableTable): Map<string, string> {
  return new Map(table.fields.map(field => [field.name.toLowerCase(), field.name]));
}

function canonicalField(canonical: Map<string, string>, logicalName: string): string {
  const lowerName = logicalName.toLowerCase();
  const alias = LOGICAL_FIELD_ALIASES.get(lowerName);
  return (alias ? canonical.get(alias) : undefined) ?? canonical.get(lowerName) ?? logicalName;
}

async function mapFieldsToAirtableNames(fields: Record<string, unknown>): Promise<Record<string, unknown>> {
  const table = await getConfiguredTable();
  const canonical = buildCanonicalFieldMap(table);
  const mapped: Record<string, unknown> = {};

  for (const [key, value] of Object.entries(fields)) {
    mapped[canonicalField(canonical, key)] = value;
  }

  return mapped;
}

function toLogicalFields(lead: LeadQualified): Record<string, unknown> {
  const reviews = lead.reviews ?? [];
  const images = lead.images ?? [];
  const details = lead.businessDetails ?? {
    priceRange: null,
    serviceOptions: [],
    diningOptions: [],
    offerings: [],
    atmosphere: [],
    accessibility: [],
    planning: [],
    rawAttributes: []
  };

  return {
    lead_id: lead.leadId,
    business_name: lead.businessName,
    category: lead.category ?? '',
    city: lead.city,
    rating: lead.rating ?? null,
    review_count: lead.reviewCount ?? null,
    maps_url: lead.source.mapsUrl,
    website_url: lead.websiteUrl ?? '',
    website_status: lead.websiteStatus,
    social_links: lead.socialLinks.join('\n'),
    fallback_channels: lead.contact.fallbackChannels.join('\n'),
    contact_email: lead.contact.email ?? '',
    contact_channel: lead.contact.channel,
    contact_confidence: lead.contact.confidence,
    lead_score: lead.score.total,
    image_count: lead.imageUrls.length,
    review_snippets: reviews.map(formatReviewSnippet).join('\n\n'),
    detail_highlights: [
      details.priceRange ? `Prix: ${details.priceRange}` : null,
      ...details.serviceOptions,
      ...details.diningOptions,
      ...details.offerings,
      ...details.atmosphere,
      ...details.accessibility,
      ...details.planning
    ].filter(Boolean).join('\n'),
    rich_data_json: JSON.stringify({
      images,
      reviews,
      businessDetails: details
    }, null, 2),
    preview_url: lead.previewUrl ?? '',
    status: lead.status,
    last_action_at: lead.lastActionAt ?? '',
    notes: lead.notes
  };
}

async function findRecordByLeadId(leadId: string): Promise<AirtableRecord | null> {
  const table = await getConfiguredTable();
  const canonical = buildCanonicalFieldMap(table);
  const leadIdField = canonical.get('lead_id');
  if (!leadIdField) {
    throw new Error('Champ Airtable manquant: lead_id. Lancez npm run lead:airtable:bootstrap.');
  }

  const url = `${getBaseUrl()}?filterByFormula=${encodeURIComponent(`{${leadIdField}}='${leadId}'`)}`;
  const response = await fetch(url, { headers: getHeaders() });
  if (!response.ok) {
    throw new Error(`Airtable lookup failed: ${response.status} ${response.statusText} — ${await response.text()}`);
  }
  const payload = (await response.json()) as { records?: AirtableRecord[] };
  return payload.records?.[0] ?? null;
}

export async function syncLeadsToAirtable(leads: LeadQualified[]): Promise<void> {
  logger.info(`📒 Sync Airtable: ${leads.length} leads`);

  for (const lead of leads) {
    const existing = await findRecordByLeadId(lead.leadId);
    const fields = await mapFieldsToAirtableNames(toLogicalFields(lead));

    const response = await fetch(existing ? `${getBaseUrl()}/${existing.id}` : getBaseUrl(), {
      method: existing ? 'PATCH' : 'POST',
      headers: getHeaders(),
      body: JSON.stringify({ fields })
    });

    if (!response.ok) {
      throw new Error(
        `Airtable sync failed for ${lead.businessName}: ${response.status} ${response.statusText} — ${await response.text()}`
      );
    }
  }
}

export async function listApprovedAirtableLeads(): Promise<AirtableRecord[]> {
  const table = await getConfiguredTable();
  const canonical = buildCanonicalFieldMap(table);
  const statusField = canonicalField(canonical, 'status');
  const contactEmailField = canonicalField(canonical, 'contact_email');
  const formula = `AND({${statusField}}='approved', {${contactEmailField}}!='')`;
  const url = `${getBaseUrl()}?filterByFormula=${encodeURIComponent(formula)}`;
  const response = await fetch(url, { headers: getHeaders() });
  if (!response.ok) {
    throw new Error(`Airtable approved list failed: ${response.status} ${response.statusText} — ${await response.text()}`);
  }
  const payload = (await response.json()) as { records?: AirtableRecord[] };
  return payload.records ?? [];
}

export async function updateAirtableAfterPreview(leadId: string, previewUrl: string): Promise<void> {
  const record = await findRecordByLeadId(leadId);
  if (!record) return;

  const response = await fetch(`${getBaseUrl()}/${record.id}`, {
    method: 'PATCH',
    headers: getHeaders(),
    body: JSON.stringify({ fields: await mapFieldsToAirtableNames({ preview_url: previewUrl, status: 'draft_ready' }) })
  });

  if (!response.ok) {
    throw new Error(`Airtable preview update failed: ${response.status} ${response.statusText} — ${await response.text()}`);
  }
}

