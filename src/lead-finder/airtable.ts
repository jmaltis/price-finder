export async function clearAirtableLeads(): Promise<number> {
  const response = await fetch(getBaseUrl(), { headers: getHeaders() });
  if (!response.ok) {
    throw new Error(`Airtable clear list failed: ${response.status} ${response.statusText} — ${await response.text()}`);
  }

  const payload = (await response.json()) as { records?: AirtableRecord[] };
  const records = payload.records ?? [];
  if (records.length === 0) return 0;

  for (let index = 0; index < records.length; index += 10) {
    const chunk = records.slice(index, index + 10);
    const query = chunk.map(record => `records[]=${encodeURIComponent(record.id)}`).join('&');
    const deleteResponse = await fetch(`${getBaseUrl()}?${query}`, {
      method: 'DELETE',
      headers: {
        Authorization: `Bearer ${getEnv('airtableApiKey')}`
      }
    });

    if (!deleteResponse.ok) {
      throw new Error(`Airtable clear failed: ${deleteResponse.status} ${deleteResponse.statusText} — ${await deleteResponse.text()}`);
    }
  }

  return records.length;
}

export async function bootstrapAirtableLeadSchema(): Promise<void> {
  const table = await getConfiguredTable();
  const existing = buildCanonicalFieldMap(table);
  const missing = LEAD_FIELD_SPECS.filter(spec => {
    const lowerName = spec.name.toLowerCase();
    const alias = LOGICAL_FIELD_ALIASES.get(lowerName);
    return !existing.has(lowerName) && !(alias && existing.has(alias));
  });

  if (missing.length === 0) {
    logger.success('Schéma Airtable déjà prêt');
  } else {
    logger.info(`🧱 Création de ${missing.length} champs Airtable dans ${table.name}`);

    for (const spec of missing) {
      const response = await fetch(`${getMetadataBaseUrl()}/tables/${table.id}/fields`, {
        method: 'POST',
        headers: getHeaders(),
        body: JSON.stringify(spec)
      });

      if (!response.ok) {
        throw new Error(
          `Création du champ Airtable "${spec.name}" échouée: ${response.status} ${response.statusText} — ${await response.text()}`
        );
      }

      logger.success(`  Champ créé: ${spec.name}`);
    }
  }

  await ensureSingleSelectChoices('lead_status');
  await ensureSingleSelectChoices('website_status');
  await ensureSingleSelectChoices('contact_channel');
  await ensureSingleSelectChoices('contact_confidence');
}

async function ensureSingleSelectChoices(fieldName: string): Promise<void> {
  const table = await getConfiguredTable();
  const field = table.fields.find(candidate => candidate.name === fieldName);
  const spec = LEAD_FIELD_SPECS.find(candidate => candidate.name === fieldName);
  const desiredChoices = spec?.options?.choices;

  if (!field || field.type !== 'singleSelect' || !Array.isArray(desiredChoices)) return;

  const existingChoices = field.options?.choices ?? [];
  const missingChoices = desiredChoices.filter(choice =>
    !existingChoices.some(existing => existing.name === choice.name)
  );
  if (missingChoices.length === 0) return;

  const mergedChoices = [...existingChoices, ...missingChoices];
  const response = await fetch(`${getMetadataBaseUrl()}/tables/${table.id}/fields/${field.id}`, {
    method: 'PATCH',
    headers: getHeaders(),
    body: JSON.stringify({
      options: {
        choices: mergedChoices.map(choice => ({
          name: choice.name,
          color: choice.color ?? 'blueLight2'
        }))
      }
    })
  });

  if (!response.ok) {
    throw new Error(
      `Mise à jour des options Airtable "${fieldName}" échouée: ${response.status} ${response.statusText} — ${await response.text()}`
    );
  }

  logger.success(`  Options mises à jour: ${fieldName} (+${missingChoices.length})`);
}
