interface AirtableRecord {
  id: string;
  fields: Record<string, unknown>;
}

interface AirtableField {
  id: string;
  name: string;
  type: string;
  options?: {
    choices?: Array<{
      id?: string;
      name: string;
      color?: string;
    }>;
  };
}

interface AirtableTable {
  id: string;
  name: string;
  fields: AirtableField[];
}

interface AirtableSchemaResponse {
  tables?: AirtableTable[];
}

interface AirtableFieldSpec {
  name: string;
  type: string;
  description?: string;
  options?: Record<string, unknown>;
}

function formatReviewSnippet(review: LeadQualified['reviews'][number]): string {
  const meta = [
    review.author,
    review.rating != null ? `${review.rating.toFixed(1)}/5` : null,
    review.relativeTime
  ].filter(Boolean).join(' · ');

  return meta ? `${meta}\n${review.text}` : review.text;
}

